    private void cmdAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdAdicionarActionPerformed
        // TODO add your handling code here:
        //Preencher o nosso modelo
        Aluno aluno = new Aluno();
        aluno.setIdAluno(Integer.parseInt(txtIdAluno.getText()));
        aluno.setNome(txtNome.getText());
        aluno.setEmail(txtEmail.getText());
        aluno.setTelefone(txtTelefone.getText());
        //Instanciar o meu objeto de negocio
        bus.adicionarAlunoLista(aluno);
        //apresentar a lista na tabela
        apresentarListaNaTabela();
        //limpar todas as caixas de texto
        limparTudo();
        txtIdAluno.requestFocus();
    }//GEN-LAST:event_cmdAdicionarActionPerformed

