    private void apresentarListaNaTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tblAlunos.getModel();
        modelo.setNumRows(0);
        for (Aluno item : bus.getLista()) {
            modelo.addRow(new Object[]{
                item.getIdAluno(),
                item.getNome(),
                item.getEmail(),
                item.getTelefone()
            });
        }
    }

    private void limparTudo() {
        for (Component item : this.getContentPane().getComponents()) {
            if(item instanceof JTextField){
                JTextField obj = (JTextField) item;
                obj.setText(null);
            }
        }
    }
